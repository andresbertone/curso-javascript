// OPERADORES MATEMÁTICOS
console.log('Operadores matemáticos');
let a = 5;
let b = 3;

console.log(a+b);
console.log(a-b);
console.log(a*b);
console.log(a/b); // división
console.log(a%b); // módulo

// OPERADOR DE CONCATENACIÓN
console.log('Operadore de concatenación');
let usuario = 'Andres'
console.log('Hola ' + usuario);

// OPERADORES DE INCREMENTO - DECREMENTO
console.log('Operadores de incremento - decremento');
let c = 10;
console.log(c++);
console.log(c);
console.log(++c);
console.log(c);

let d = 5;
console.log(d--);
console.log(d);
console.log(--d);
console.log(d);

// OPERADORES DE ASIGNACIÓN
console.log('Operadores de asignación');
let e = 10;

e = e + 3;
console.log(e);

e -= 2;
console.log(e);

e *= 3;
console.log(e);

e /= 5;
console.log(e);

e %= 2;
console.log(e);